/*Sum Of Three Number Enter BY User*/
#include<stdio.h>
main()
{
	int a , b , c , sum;
	
	printf(" Enter Value Of A:- ");
	scanf("%d",&a);
	printf(" Enter Value of B:- ");
	scanf("%d",&b);
	printf(" Enter Value of C:- ");
	scanf("%d",&c);
	
	sum=a+b+c;
	
	printf("Enter Value %d + %d + %d :- %d ",a,b,c,sum);
	
}


