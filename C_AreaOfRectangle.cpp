/*Area Of Rectangle*/

#include<stdio.h>
main()
{
	int Area , Length , Width ;
	
	printf("Enter The Length Of Rectangle : ");
	scanf("%d",&Length);
	
	printf("Enter The Width Of Rectangle : ");
	scanf("%d",&Width);
	
	Area=Length*Width;
	
	printf("Area Of Rectangle Is : %d",Area);
	
	
}
